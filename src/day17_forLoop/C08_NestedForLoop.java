package day17_forLoop;

public class C08_NestedForLoop {

	public static void main(String[] args) {
		// 1' den 4 e kadar sayilý aralarýnda 1 nosluk býrakarak yazdýrýn

		for (int satir = 1; satir <=3;  satir++) {
			
		
			for (int i = 1; i <=5;  i++) {
				System.out.print(satir*i +" "); //bu sekilde yapýyoruz
		
		 
		}
		System.out.println("");
		}		
		
	

}}
